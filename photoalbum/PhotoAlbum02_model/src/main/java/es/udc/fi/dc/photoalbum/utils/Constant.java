package es.udc.fi.dc.photoalbum.utils;

/**
 */
public abstract class Constant {

    static private final String username = "public";
    static private final int id = 1;

    /**
     * Method getUsername.
     * 
     * @return String
     */
    static public String getUsername() {
        return username;
    }

    /**
     * Method getId.
     * 
     * @return int
     */
    public static int getId() {
        return id;
    }
}
